public class Casse {
    public static void main(String args[]) {
        int choice = 2;
        switch (choice) {
            case 1:
                System.out.println("Circle");
                break;
            case 2:
                System.out.println("Rectangle");
                break;
            case 3:
                System.out.println("Square");
                break;
            default:
                System.out.println("No choice");
        }
    }
}
